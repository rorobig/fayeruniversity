<h3 class="il">Program Info</h3>
<div class= "lightr">
<p> The legal profession is a dynamic and well-paid sector that demands employees with the most up-to-the-minute skills and education. Paralegals are a vital element to any legal team who are well-respected for their knowledge of legal documentation and procedures.</p>
<p>FU's Paralegal diploma program teaches a combination of administrative skills with specific paralegal training. Students are certified in internet and core computing, and study day-to-day administrative skills like advanced keyboarding and business communications. Study of the law and legal processes includes an introduction to the Canadian court system, including elements of corporate law, family law, real estate, criminal law, and litigation. Students also learn how to handle evidence and legal documentation.</p>
<p> The paralegal training at FU includes a five-week practicum where students will put their newly-acquired skills to work in a legal office.</p>

<h3 class="il">Program Info</h3>
<div class= "lightr">
<p>The Law Clerk with Internship program at FU prepares students to pursue a traditional career path as a law clerk, working under the direct supervision of a lawyer.  </p>
<p> Law clerks are able to work in several areas of law including criminal law, family law, civil litigation, environmental law, corporate law, healthcare, real estate and wills and estates.</p>
<p> Students in the Law Clerk with Internship program will have the opportunity to participate in a five-week internship in a law office or other legal environment, giving them invaluable experience that can be added to their resume. </p>
<p> In addition to their Law Clerk with Intership diploma, graduates can apply to write the four Institute of Law Clerks of Ontario (ILCO) exams.</p>

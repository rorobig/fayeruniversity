<h3 class="il">Program Info</h3>
<div class= "lightr">
<p>If you’re looking for a job that’s never dull, 3D Modeling Animation is the career for you. 3D modelers and animators work on a huge range of projects, 
from blockbuster movies and beloved TV shows to cult-classic video games and addictive mobile apps. 
But before you can jump into this exciting career, you’re going to need industry-recognized training. </p>

<p>The 3D Modeling Animation Art and Design program at FU College focuses on providing graduates with a solid foundation in the technical and 
artistic skills needed to be successful in the game, animation and CGI industries.
 Key to this is a genuine understanding of storytelling, game play, modeling and animation techniques. </p>

 <p>Students will first build their skill set in traditional artistic techniques like color theory, design fundamentals and life drawing,
 and will apply this knowledge to the production and animation of characters, lighting, texturing, layout and design. Students will be able to apply industry standard software tools and techniques to production processes as well. 
 By the end of the program, students will have an industry-standard portfolio ready to show to potential employers.</p>

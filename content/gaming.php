<p>In order to be successful in the game, animation and CGI industries, students need to master key storytelling, game design, game play,
 modeling and animation techniques. Luckily, that’s exactly what's covered in the Game Development and Design program at FU.</p>

</p> From color theory and design fundamentals to life drawing and character design,
students will be able to apply industry standard software tools and techniques to production processes, mimicking the type of work they’ll do in their new careers. </p>


<h3 class="il">Program Info</h3>
<div class= "lightr">
<p> FU's Advanced Massage Therapy diploma program teaches you the advanced skills you need to compete for the best massage therapy jobs. Professional massage therapists specialize in relaxation, wellness and recovery.</p>
<p> A prerequisite for this diploma program requires successful completion of FU's Massage Therapy program, or equivalent, with prior learning assessment. The program begins by building on systems anatomy and remedial exercise, pathophysiology, neuroanatomy, and anatomy palpation. Students will also learn about business and law as it pertains to massage therapy, as well as building their knowledge of industry ethics and professional communications.
</p>
<p>Students gain hands-on experience through their required practicum placements. Graduates are encouraged to become registered with the Natural Health Practitioners of Canada Association, the Remedial Massage Therapist Society and the Massage Therapist Association of Alberta.
 </p>
<p>Unlike many massage therapy schools, FU supports our graduates in finding employment after they have completed their program. </p>

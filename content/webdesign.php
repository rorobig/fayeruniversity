<h3 class="il">Program Info</h3>
<div class= "lightr">

<p> The Web Design program is designed to train professionals for positions as web page designer, 
website architect, web developer, website developer, web programmer, web designer or webmaster. </p>

<p>The Web Design program is comprised of two important components: a technical component –
students acquire the skills needed in web programming, including training in the use of several software, in programming, in database and data management, 
and in server related technologies – 
and a creative component which allows them to develop their artistic ability through principles of design, colour and image processing.</p>

<p> The program features several assignments, exercises and projects, including the creation of a professional portfolio. 
The projects undertaken are directly linked to the true nature of the work and they learn in 
realistic working conditions in order to foster the development of the required skills and attitudes. 
</p>

<h3 class="il">Program Info</h3>
<div class= "lightr">
<p >The Graphic Design program in Alberta provides students with the solid technical and creative foundation they need to thrive in the graphic design industry.
 Providing students with training in the latest design software, like Photoshop, Illustrator, and Dreamweaver,
  the Graphic Design program prepares graduates for the demands of today’s workplaces. </p>


<p> Students cover drawing, colour theory, and typography while working on projects ranging from business cards to magazine covers. 
Over the course of the program, students will build a strong portfolio that will showcase their individual style with websites, packaging, advertising, and branding.</p>

 <p> Graduates of this program will be prepared for graphic design jobs in a wide range of settings like
  newspapers, magazines, design firms, art departments, and much more! 


